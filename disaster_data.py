def get_risk_profile(country):
    if country in RISK_PROFILES:
        return RISK_PROFILES[country]
    return {d: 3 for d in DISASTER_TYPES}


def get_overall_risk(country):
    profile = get_risk_profile(country)
    if not profile:
        return 3
    return round(sum(profile.values()) / len(profile), 1)


def get_top_risks(country, n=3):
    profile = get_risk_profile(country)
    sorted_risks = sorted(profile.items(), key=lambda x: x[1], reverse=True)
    return sorted_risks[:n]


